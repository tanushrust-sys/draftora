type PromptBank = Record<string, string[]>;

const MIN_PROMPTS_PER_SECTION = 200;
const TOPICS_PER_SECTION = 150;

function normalizePrompt(prompt: string) {
  return prompt.trim().replace(/\s+/g, ' ').toLowerCase();
}

function uniquePrompts(prompts: string[]) {
  const seen = new Set<string>();
  return prompts.filter((prompt) => {
    const key = normalizePrompt(prompt);
    if (!key || seen.has(key)) return false;
    seen.add(key);
    return true;
  });
}

function buildPromptSeries(
  topics: string[],
  templates: Array<(topic: string) => string>,
) {
  return uniquePrompts(
    templates.flatMap((template) => topics.map((topic) => template(topic))),
  );
}

function buildSection(topics: string[], templates: Array<(topic: string) => string>) {
  return buildPromptSeries(topics, templates).slice(0, MIN_PROMPTS_PER_SECTION);
}

function expandTopicBank(baseTopics: string[], expansions: string[]) {
  return uniquePrompts([
    ...baseTopics,
    ...baseTopics.flatMap((topic) => expansions.map((expansion) => `${topic} ${expansion}`)),
  ]).slice(0, TOPICS_PER_SECTION);
}

